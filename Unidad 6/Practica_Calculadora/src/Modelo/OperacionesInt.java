package Modelo;

public class OperacionesInt {

	public OperacionesInt() {
	}

	public int calcularSuma(int operando1, int operando2) {
		int resultado = operando1 + operando2;

		return resultado;

	}

	public int calcularResta(int operando1, int operando2) {
		int resultado = operando1 - operando2;

		return resultado;
	}

	public int calcularMultiplicacion(int operando1, int operando2) {
		int resultado = operando1 * operando2;

		return resultado;
	}

	public int calcularDivision(int operando1, int operando2) {
		int resultado = operando1 / operando2;

		return resultado;
	}

}
