package Modelo;

public class OperacionesDouble {

	public OperacionesDouble() {
	}

	public double calcularSuma(double operando1, double operando2) {
		double resultado = operando1 + operando2;

		return resultado;
	}

	public double calcularResta(double operando1, double operando2) {
		double resultado = operando1 - operando2;

		return resultado;
	}

	public double calcularMultiplicacion(double operando1, double operando2) {
		double resultado = operando1 * operando2;

		return resultado;
	}

	public double calcularDivision(double operando1, double operando2) {
		double resultado = operando1 / operando2;

		return resultado;
	}
}
